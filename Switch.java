public class Switch{
    public static void main(String args[]){
        char CatagoryCode = 'Z';
        switch(CatagoryCode){
            case 'E':
                System.out.println("electronics");
                break;
                
            case 'C':
                System.out.println("clothing");
                break;
                
            default:
            System.out.println("Unknown");
            
        }
        
    }
}
